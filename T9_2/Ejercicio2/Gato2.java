package T9_2.Ejercicio2;

public class Gato2 {
  private String nombre;
  private int edad;
  private String raza;

  public Gato2(String nombre, int edad, String raza) {
    this.nombre = nombre;
    this.edad = edad;
    this.raza = raza;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

  public int getEdad() {
    return edad;
  }

  public void setEdad(int edad) {
    this.edad = edad;
  }

  public String getRaza() {
    return raza;
  }

  public void setRaza(String raza) {
    this.raza = raza;
  }

}

